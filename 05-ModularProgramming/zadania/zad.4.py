import math
x = 3.7
y = 4
def sqrt(x):
    sqrtX = math.sqrt(x)
    return sqrtX
print(sqrt(x))


def potega(x,y):
    pot = x**y
    return pot
print(potega(x,y))


def pierw(x,y):
    pw = x**(1/y)
    return pw
print(pierw(x,y))


def pk(y):
    pi = 3.14
    pole = pi*(y**2)
    return pole
print(pk(y))


def silnia(y):
    silnia = math.factorial(y)
    return silnia
print(silnia(y))


